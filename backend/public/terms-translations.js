let currentLang = 'ru';

function changeLanguage() {
    const select = document.getElementById('languageSelect');
    currentLang = select.value;
    
    document.documentElement.lang = currentLang;
    document.documentElement.setAttribute('data-lang', currentLang);
    
    updateTranslations();
    localStorage.setItem('snapcal-lang', currentLang);
}

function updateTranslations() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (termsTranslations[currentLang] && termsTranslations[currentLang][key]) {
            element.textContent = termsTranslations[currentLang][key];
        }
    });
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Å–Ω–æ–≤–Ω–æ–π –∫–æ–Ω—Ç–µ–Ω—Ç
    updateMainContent();
}

function updateMainContent() {
    const contentElement = document.getElementById('terms-main-content');
    if (contentElement && termsContent && termsContent[currentLang]) {
        contentElement.innerHTML = termsContent[currentLang];
    }
}

document.addEventListener('DOMContentLoaded', function() {
    // –ß–∏—Ç–∞–µ–º —è–∑—ã–∫ –∏–∑ URL –ø–∞—Ä–∞–º–µ—Ç—Ä–∞
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    
    // –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç: URL –ø–∞—Ä–∞–º–µ—Ç—Ä -> localStorage -> ru –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
    const savedLang = localStorage.getItem('snapcal-lang') || 'ru';
    currentLang = urlLang || savedLang;
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —á—Ç–æ —è–∑—ã–∫ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è
    if (!termsTranslations[currentLang]) {
        currentLang = 'ru';
    }
    

    
    const languageSelect = document.getElementById('languageSelect');
    languageSelect.value = currentLang;
    languageSelect.addEventListener('change', changeLanguage);
    
    changeLanguage();
});

// Smooth scrolling for anchor links
document.addEventListener('DOMContentLoaded', function() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
}); 