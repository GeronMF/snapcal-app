let currentLang = 'ru';

function changeLanguage() {
    const select = document.getElementById('languageSelect');
    currentLang = select.value;
    document.documentElement.lang = currentLang;
    document.documentElement.setAttribute('data-lang', currentLang);
    updateTranslations();
    updateMetaTags();
    localStorage.setItem('snapcal-lang', currentLang);
}

function updateTranslations() {
    console.log('Updating translations for language:', currentLang);
    const elements = document.querySelectorAll('[data-translate]');
    console.log('Found elements with data-translate:', elements.length);
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
            console.log('Translated:', key, '->', translations[currentLang][key]);
        } else {
            console.log('Translation not found for key:', key, 'in language:', currentLang);
        }
    });
}

