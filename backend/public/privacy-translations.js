let currentLang = 'ru';

function changeLanguage() {
    const select = document.getElementById('languageSelect');
    currentLang = select.value;
    
    document.documentElement.lang = currentLang;
    document.documentElement.setAttribute('data-lang', currentLang);
    
    updateTranslations();
    localStorage.setItem('snapcal-lang', currentLang);
}

function updateTranslations() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (privacyTranslations[currentLang] && privacyTranslations[currentLang][key]) {
            element.textContent = privacyTranslations[currentLang][key];
        }
    });
    
    // Обновляем основной контент
    updateMainContent();
}

function updateMainContent() {
    const contentElement = document.getElementById('privacy-main-content');
    if (contentElement && privacyContent && privacyContent[currentLang]) {
        contentElement.innerHTML = privacyContent[currentLang];
    }
}

document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('snapcal-lang') || 'ru';
    currentLang = savedLang;
    const languageSelect = document.getElementById('languageSelect');
    languageSelect.value = savedLang;
    languageSelect.addEventListener('change', changeLanguage);
    
    changeLanguage();
});

// Smooth scrolling for anchor links
document.addEventListener('DOMContentLoaded', function() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
}); 